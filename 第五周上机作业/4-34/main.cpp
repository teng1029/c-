#include <iostream>

using namespace std;

int main()
{
    int a = 0;
    int factorialofa = 0;

    cout << "Enter a nonnegative integer:\n";
    cin >> a;

    unsigned int counter = a-1;
    while( --counter = 0 )
    {
        factorialofa = a * counter;
        a== a - 1;
    }
    cout << factorialofa << endl;
}

